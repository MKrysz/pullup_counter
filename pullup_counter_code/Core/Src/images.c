void Image_Draw(uint8_t x, uint8_t y, image_t image)
{
    for(size_t i = 0; i<image.size; i++){
        uint8_t tempX, tempY;
        tempX = image.data[i][0];
        tempY = image.data[i][1];
        ssd1306_DrawPixel(x+tempX, y+tempY, White);
    }
}

